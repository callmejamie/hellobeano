    function validateForm(formObj) {
      formObj.validate({
        rules: {
            name: {
              required: true
            },
            email: {
              required: true,
              email: true
            },
            message: {
              required: true
            }
          },
          messages: {
            name: 'Please fill in your name',
            email: 'Please provide a valid email address',
            message: 'Oops, looks like your message is empty. What\'s the question you\'d like to ask us?'
          }
      });
    }

    $(document).ready(function(){

      $('#send-email').click(function(){
        var form = $('#contactForm')
        validateForm(form);

        if($('#contactName').valid() && $('#contactEmail').valid() && $('#contactMessage').valid()) {
          var postData = $(form).serializeArray();

          $.ajax({
            url:'/email.php',
            type: 'POST',
            data: postData,
            success: function(data) {
              var message = '';
              if(data == 200) {
                message = "<div class='alert alert-success'>Message sent successfully!</span>";
              }
              if(data == 400) {
                message = "<div class='alert alert-danger'>Oops, something went wrong. Please send your message directly to jamiel[at]hellobeano.com</div>";
              }
              $('#form-notification').html(message);
            }
          });
        } else {
          $('#form-notification').html('<div class="alert alert-danger">Looks like there\'s some errors in the fields above. Please fix them before sending your message again</div>')
        }
      });

    });
  </script>
